namespace Aviat\Ion;

/**
 * Joins paths together. Variadic to take an
 * arbitrary number of arguments
 *
 * @param string ...$args
 * @return string
 */
function _dir(string ...$args): string
{
	return implode(DIRECTORY_SEPARATOR, $args);
}